animal = "  Gato feliz   "
print(animal.upper()) # Upper convierte el string en mayuscula

print(animal.lower()) # Lower convierte el string en minuscula

print(animal.strip().capitalize()) # capitalize convierte la primera letra en mayuscula y el resto en minuscula

print(animal.title()) # Title convierte la primera letra de cada palabra en mayuscula

print(animal.strip()) # Strip elimina todos los espacios del string de la izquierda y derecha

print(animal.rstrip()) # Rstrip quita los espacios de la derecha

print(animal.find("liz")) # find devuelve el indice de la cadena de caracteres / si devuelve -1 quiere decir que no existe

print(animal.replace("feliz", "tierno")) # replace remplaza los caracteres

print("feliz" in animal) # podemos saber si esa cadena de caracteres se encuentra en el string / devuelve un boolean

print("feliz" not in animal) # podemos saber si esa cadena de caracteres no se encuentra en el string / devuelve un boolean





